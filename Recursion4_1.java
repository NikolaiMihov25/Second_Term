package edu;

public class Recursion4_1 {
	 public static int foo (int x, int y)
	    {
	       if (y == 0)
	             return x;
	       else
	             return foo(y, x % y);
	    }
//	 методът връща първото подадено число, ако второто е раво на нула. Ако трърдението у == 0 е грешно методът се преизпълнява, 
//	 като второто пададено число (у), заемя мастото на х, а мястото на игрек се запълва от остатъка на делението на х с у.
}
